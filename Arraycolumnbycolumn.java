public class Arraycolumnbycolumn {
    public static void main(String[] args) {
        int[][] twoD = {{5, 6, 7}, {8, 9, 10}};
        for(int i=0;i<2;i++) {
            for(int j=0;j<3;j++) {
                System.out.print(twoD[i][j]+" ");
            }
            System.out.println();
        }
    }
    }


